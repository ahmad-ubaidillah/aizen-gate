import { expect, test, describe, vi, afterEach } from 'vitest';
const fs = require('fs-extra');
const { runBenchmark } = require('../scripts/benchmark');

describe('Aizen-Gate Benchmark Evaluation', () => {
    
    test('Calculates perfect score when all artifacts are valid', async () => {
        const mockRoot = '/tmp/project';
        const mockBoard = `| ID | Task | Depends On | Agent | Status | Expectation |`;
        const mockProject = `## Inferred Architecture\n*Generated by \`sa-map\`*\n`;
        const mockState = `**Current focus:** none\n**Last activity:** none`;

        vi.spyOn(fs, 'existsSync').mockReturnValue(true);
        vi.spyOn(fs, 'readFileSync').mockImplementation((p) => {
            if (p.includes('board.md')) return mockBoard;
            if (p.includes('project.md')) return mockProject;
            if (p.includes('state.md')) return mockState;
            return '';
        });

        const result = await runBenchmark(mockRoot);
        
        expect(result.success).toBe(true);
        expect(result.score).toBe(100);
    });

    afterEach(() => {
        vi.restoreAllMocks();
    });
});
